static int last_root_iterations = 0; // Эта переменная хранит количество итераций последнего вызова root

// Константы точности и ограничений
static const double EPS_ROOT = 1e-7; // точность для поиска корней
static const double EPS_INTEGRAL = 1e-7; // точность для интегралов
static const int MAX_ROOT_ITERATIONS = 100000; // максимум итераций для root, чтобы программа не зависла навсегда
static const int MAX_INTEGRAL_N = 1 << 27; // максимальное число разбиений для интеграла


// Эта структура хранит информацию об одном корне, то есть об одной точке пересечения двух функций.
typedef struct {
    double x; // абсцисса точки пересечения.
    // номера функций которые пересекаются
    // 0 = f1
    // 1 = f2
    // 2 = f3
    int first; 
    int second;
    int iterations; // сколько итераций потребовалось для поиска этого корня.
    const char *name; // имя корня для вывода, например x12 x13 x23
} RootInfo;


static double diff_value(afunc *f, afunc *g, double x) {
    // функция считатет разность значений двух функций в точке x
    // Аргмуенты: 
    //     afunc *f, afunc *g - функции
    //     double x - точка в которой ищем разность
    // Функция возвращает double - разность
    return f(x) - g(x);
}

static double diff_derivative(afunc *df, afunc *dg, double x) {
    // функция считатет разность значений производных двух функций в точке x
    //Эта функция нужна для метода Ньютона.
    // Аргмуенты: 
    //     afunc *df, afunc *dg - производные функций
    //     double x - точка в которой ищем разность
    // Функция возвращает double - разность
    return df(x) - dg(x);
}




double root(afunc *f, afunc *g, afunc *df, afunc *dg, double a, double b, double eps) {

    // Ищет точку пересечения двух функций f и g на отрезке [a, b].
    //
    // Фактически решается уравнение:
    //
    //     f(x) = g(x)
    //
    // как:
    //
    //     F(x) = f(x) - g(x) = 0
    //
    // Используется метод Ньютона/касательных.

    // Метод Ньютона делает так:
    // 1. Берём начальное приближение x.
    // 2. Строим касательную к графику функции в точке x.
    // 3. Смотрим, где эта касательная пересекает ось Ox.
    // 4. Это пересечение берём как новое приближение.
    // 5. Повторяем.

    // Производная F'(x) считается как:
    //
    //     F'(x) = df(x) - dg(x)
    //
    // Аргументы:
    // f   — первая функция;
    // g   — вторая функция;
    // df  — производная первой функции;
    // dg  — производная второй функции;
    // a   — левая граница отрезка;
    // b   — правая граница отрезка;
    // eps — требуемая точность.
    //
    // Возвращает:
    // приближённое значение x, при котором f(x) примерно равно g(x).


    // Обнуляем счётчик итераций перед новым запуском root().
    // Потом это значение используется для вывода через --iterations.
    last_root_iterations = 0;


    // Считаем значения F(a) и F(b),
    // где F(x) = f(x) - g(x).
    double fa = diff_value(f, g, a);
    double fb = diff_value(f, g, b);

    // Если левая граница уже является корнем,
    // сразу возвращаем a.
    if (fabs(fa) <= eps) {
        return a;
    }

    // Если правая граница уже является корнем,
    // сразу возвращаем b.
    if (fabs(fb) <= eps) {
        return b;
    }

    // lo и hi — текущий отрезок, внутри которого ищем корень.
    // flo и fhi — значения F(lo) и F(hi).
    // В процессе работы отрезок будет постепенно сужаться.
    double lo = a;
    double hi = b;
    double flo = fa;
    double fhi = fb;

    // fm — значение функции F в середине исходного отрезка.
    double fm = diff_value(f, g, 0.5 * (a + b));

    // chord_mid — значение хорды в середине отрезка.
    double chord_mid = 0.5 * (fa + fb);

    // Выбираем начальное приближение x для метода Ньютона.
    //
    // Метод касательных зависит от того, с какого конца отрезка начать.
    // Обычно выбирают конец, где выполняется условие:
    //
    //     F'(x) * F''(x) > 0
    //
    // Вторую производную здесь явно не считаем.
    // Вместо этого примерно определяем характер графика:
    //
    //     fb > fa  — функция в целом возрастает;
    //     fb < fa  — функция в целом убывает;
    //
    //     fm <= chord_mid — график в середине ниже хорды;
    //     fm >= chord_mid — график в середине выше хорды.
    //
    // Если функция возрастает и лежит ниже хорды
    // или функция убывает и лежит выше хорды,
    // начинаем с правого конца b.
    // В остальных случаях начинаем с левого конца a.
    double x;
    if (((fb > fa) && (fm <= chord_mid)) || ((fb < fa) && (fm >= chord_mid))) {
        x = b;
    } else {
        x = a;
    }

    // Основной цикл метода Ньютона.
    for (int it = 1; it <= MAX_ROOT_ITERATIONS; ++it) {
        // Значение F(x) = f(x) - g(x) в текущей точке.
        double fx = diff_value(f, g, x);

        // Значение производной F'(x) = df(x) - dg(x) в текущей точке.
        double dfx = diff_derivative(df, dg, x);

        // nx — новое приближение к корню.
        double nx;

        // Проверяем, можно ли безопасно делать шаг Ньютона.
        //
        // Если fx или dfx не являются нормальными конечными числами,
        // либо производная слишком близка к нулю,
        // то формула Ньютона может дать плохой результат.
        //
        // В таком случае используем запасной вариант:
        // берём середину текущего отрезка [lo, hi].
        if (!isfinite(fx) || !isfinite(dfx) || fabs(dfx) < 1e-15) {
            nx = 0.5 * (lo + hi);
        } else {
            // Обычный шаг метода Ньютона:
            //     nx = x - F(x) / F'(x)

            // Геометрически nx — это точка,
            // где касательная к графику F в точке x пересекает ось Ox.
            nx = x - fx / dfx;

            // Если шаг Ньютона дал NaN, бесконечность
            // или вышел за границы текущего отрезка,
            // такой шаг считаем ненадёжным.
            //
            // Тогда снова берём середину [lo, hi].
            if (!isfinite(nx) || nx <= lo || nx >= hi) {
                nx = 0.5 * (lo + hi);
            }
        }

        // Считаем значение F(nx) в новом приближении.
        double fnx = diff_value(f, g, nx);

        // Сохраняем номер последней выполненной итерации.
        last_root_iterations = it;

        // Сужаем текущий отрезок [lo, hi].
    
        // Если F(lo) и F(nx) имеют разные знаки,
        // то корень лежит между lo и nx.
        // Значит правую границу переносим в nx.
        if (flo * fnx <= 0.0) {
            hi = nx;
            fhi = fnx;
        }

        // Иначе, если F(hi) и F(nx) имеют разные знаки,
        // то корень лежит между nx и hi.
        // Значит левую границу переносим в nx.
        else if (fhi * fnx <= 0.0) {
            lo = nx;
            flo = fnx;
        }

        // Проверяем условия остановки.
        //
        // 1. fabs(fnx) <= eps:
        //    значение F(nx) почти равно нулю.
        //
        // 2. fabs(hi - lo) <= eps:
        //    текущий отрезок стал достаточно маленьким.
        //
        // 3. fabs(nx - x) <= eps:
        //    новое приближение почти не отличается от предыдущего.
        //
        // Если выполнено хотя бы одно условие,
        // считаем, что корень найден.
        if (fabs(fnx) <= eps || fabs(hi - lo) <= eps || fabs(nx - x) <= eps) {
            return nx;
        }

        // Переходим к следующей итерации:
        // новое приближение становится текущим.
        x = nx;
    }

    // Если достигнут максимум итераций,
    // возвращаем последнее полученное приближение.
    return x;
}

static double rectangle_sum(afunc *f, double a, double b, int n) {
    // Считает приближённый интеграл функции f на отрезке [a, b]
    // методом средних прямоугольников.
    //
    // 1. Делим отрезок [a, b] на n равных частей.
    // 2. На каждой маленькой части строим прямоугольник.
    // 3. Высота прямоугольника равна значению функции в выбранной точке.
    // 4. Складываем площади всех прямоугольников.
    //
    // Формула:
    //
    //     h = (b - a) / n
    //     integral ≈ h * sum(f(a + (i + 0.5) * h))
    //
    // Аргументы:
    // f — функция, которую интегрируем;
    // a — левая граница интегрирования;
    // b — правая граница интегрирования;
    // n — количество прямоугольников.
    //
    // Возвращает:
    // приближённое значение интеграла функции f на [a, b].


    double h = (b - a) / n; // ширина прямоугольников
    double sum = 0.0; 
    // 
    for (int i = 0; i < n; ++i) {
        sum += f(a + (i + 0.5) * h); // взять середину i-го маленького отрезка и посчитать там значение функции
                                     // прибавить потом к sum
    }
    return sum * h;
}


double integral(afunc *f, double a, double b, double eps) {
    // Считает интеграл функции f на отрезке [a, b]
    // вызывая rectangle_sum с n = 16, 32, 64, ..., 
    // пока разница между соседними приближениями не станет достаточно маленькой

    // Аргументы:
    // f   — функция, которую интегрируем;
    // a   — левая граница интегрирования;
    // b   — правая граница интегрирования;
    // eps — требуемая точность.

    // Возвращает:
    // приближённое значение интеграла ∫[a,b] f(x) dx


    int sign = 1;
    
    // Если границы одинаковые, длина отрезка равна нулю,
    // значит интеграл тоже равен нулю.
    if (a == b) {
        return 0.0;
    }

    // Если границы переданы в обратном порядке,
    // меняем их местами.
    //
    // При этом запоминаем знак -1, потому что:
    //     ∫[a,b] f(x) dx = -∫[b,a] f(x) dx
    // если a > b.
    if (a > b) {
        double t = a; a = b; b = t;
        sign = -1;
    }

    // Начинаем с 16 прямоугольников.
    int n = 16;
    // Считаем первое приближение интеграла.
    double prev = rectangle_sum(f, a, b, n);

    // Далее постепенно увеличиваем точность:
    // каждый раз удваиваем количество прямоугольников.
    while (n <= MAX_INTEGRAL_N / 2) {
        n *= 2;
        // Новое, более точное приближение интеграла.
        double cur = rectangle_sum(f, a, b, n);

        // Оценка ошибки по правилу Рунге.
        // Для метода средних прямоугольников порядок точности p = 2,
        // Значит оценка ошибки:
        //     |I_2n - I_n| / 3
        // Если она меньше eps, точность считается достаточной.
        if (fabs(cur - prev) / 3.0 <= eps) {
            return sign * cur;
        }
        prev = cur;
    }
    return sign * prev;
}

static int root_cmp(const void *a, const void *b) {
    // это компаратор для сортировки массива RootInfo по значению корня x.

    // Аргументы:
    // a — указатель на первый элемент массива;
    // b — указатель на второй элемент массива.
    //
    // Так как qsort передаёт элементы как void*,
    // внутри функции мы приводим их обратно к RootInfo*.

    // Возвращает:
    // отрицательное число, если первый корень меньше второго;
    // 0, если корни равны;
    // положительное число, если первый корень больше второго.

    // Это позволяет qsort отсортировать корни по возрастанию x.
    const RootInfo *ra = (const RootInfo *)a;
    const RootInfo *rb = (const RootInfo *)b;
    return (ra->x > rb->x) - (ra->x < rb->x);
}

static void find_roots(RootInfo roots[3]) {
    // Находит три попарных пересечения функций:
    //
    //     x12: f1(x) = f2(x)
    //     x13: f1(x) = f3(x)
    //     x23: f2(x) = f3(x)
    //
    // Аргументы:
    // roots — массив из трёх элементов RootInfo,
    //         куда функция запишет найденные корни
    //
    // Возвращает:
    // ничего не возвращает
    //
    // Что меняет:
    // заполняет массив roots:
    //     roots[i].x          — найденный корень;
    //     roots[i].first      — номер первой функции;
    //     roots[i].second     — номер второй функции;
    //     roots[i].iterations — количество итераций root;
    //     roots[i].name       — имя корня: "x12", "x13" или "x23"

    // Массив указателей на функции f1, f2, f3.
    afunc *funcs[3] = {f1, f2, f3};
    // Массив указателей на производные df1, df2, df3.
    afunc *dfuncs[3] = {df1, df2, df3};

    // left_ids и right_ids задают пары функций,
    // для которых нужно найти пересечения.
    //
    // i = 0: left_ids[0] = 0, right_ids[0] = 1 -> f1 и f2
    // i = 1: left_ids[1] = 0, right_ids[1] = 2 -> f1 и f3
    // i = 2: left_ids[2] = 1, right_ids[2] = 2 -> f2 и f3
    int left_ids[3] = {0, 0, 1};
    int right_ids[3] = {1, 2, 2};

    // Имена корней для вывода пользователю.
    const char *names[3] = {"x12", "x13", "x23"};

    // Проходим по трём парам функций и для каждой пары вызываем root().
    for (int i = 0; i < 3; ++i) {
        int a = left_ids[i];
        int b = right_ids[i];

        // Ищем корень уравнения funcs[a](x) = funcs[b](x)
        roots[i].x = root(funcs[a], funcs[b], dfuncs[a], dfuncs[b], SPEC_A, SPEC_B, EPS_ROOT);
        // Сохраняем номера функций, которые образуют этот корень.
        roots[i].first = a;
        roots[i].second = b;
        // root записывает количество итераций в last_root_iterations.
        // Здесь мы сохраняем это значение в информацию о корне.
        roots[i].iterations = last_root_iterations;
        // Сохраняем имя корня: x12, x13 или x23.
        roots[i].name = names[i];
    }
}

static int pair_contains(const RootInfo *r, int function_id) {
    // Проверяет, входит ли функция с номером function_id
    // в пару функций, которые образуют данный корень.
    //
    // Аргументы:
    // r — указатель на информацию о корне;
    // function_id — номер функции, которую проверяем.
    //
    // Номера функций:
    //     0 - f1
    //     1 - f2
    //     2 - f3
    //
    // Возвращает:
    // 1, если функция участвует в этом корне;
    // 0, если не участвует.
    return r->first == function_id || r->second == function_id;
}

static int interval_functions(const RootInfo roots[3], double left, double right, int out[2]) {
    // На каждом таком промежутке фигуру ограничивают ровно две из трёх функций f1, f2, f3. 
    // Вот эта функция и пытается найти их номера.

    // Аргументы:
    // roots — массив из трёх корней:
    //         x12, x13, x23;
    // left  — левая граница текущего промежутка;
    // right — правая граница текущего промежутка;
    // out   — массив из двух элементов,
    //         куда будут записаны номера найденных функций.
    // Возвращает:
    // 1, если удалось найти ровно две функции;
    // 0, если определить две функции не удалось.
    //
    // Что меняет:
    // записывает номера найденных функций в out[0] и out[1].
    double mid = 0.5 * (left + right);
    int count = 0;

    for (int f = 0; f < 3; ++f) {
        double lo = DBL_MAX;
        double hi = -DBL_MAX;
        for (int r = 0; r < 3; ++r) {
            if (pair_contains(&roots[r], f)) {
                if (roots[r].x < lo) lo = roots[r].x;
                if (roots[r].x > hi) hi = roots[r].x;
            }
        }        
        // Если середина текущего промежутка лежит между двумя корнями,
        // в которых участвует функция f, значит эта функция проходит
        // по границе фигуры на данном промежутке.
        // 1e-9 добавлено как маленький запас от ошибок double.
        if (mid >= lo - 1e-9 && mid <= hi + 1e-9) {
            if (count < 2) out[count] = f;
            ++count;
        }
    }
    return count == 2;
}

static double area(void) {
    
    // Эта функция считает площадь фигуры, ограниченной тремя функциями f1, f2, f3.
    // У неё нет аргументов потому что она использует уже известные глобальные функции:
    // f1 f2 f3 - и их пересечения ищет внутри себя.

    // Возвращает: double
    // то есть приближённое значение площади.

    // Как работает:
    // 1. Находит три точки пересечения функций:
    //        x12: f1 = f2
    //        x13: f1 = f3
    //        x23: f2 = f3
    //
    // 2. Сортирует эти точки по координате x.
    //
    // 3. Три точки пересечения делят фигуру на два промежутка:
    //        [x_left, x_middle]
    //        [x_middle, x_right]
    //
    // 4. На каждом промежутке определяет,
    //    какие две функции являются границами фигуры.
    //
    // 5. Считает разность интегралов этих двух функций.
    //    Модуль этой разности даёт площадь части фигуры.
    //
    // 6. Складывает площади двух частей.


    // Массив указателей на функции.
    afunc *funcs[3] = {f1, f2, f3};

    // roots — исходный массив найденных корней: x12, x13, x23.
    RootInfo roots[3];

    // sorted — копия массива roots, которая будет отсортирована по x.
    RootInfo sorted[3];


    // Здесь будет накапливаться итоговая площадь.
    double result = 0.0;

    // Находим три попарных пересечения функций.
    find_roots(roots);


    // Копируем roots в sorted, чтобы отсортировать корни по координате x.
    memcpy(sorted, roots, sizeof(sorted));

    // Сортируем корни по возрастанию x.
    qsort(sorted, 3, sizeof(sorted[0]), root_cmp);

    // После сортировки есть два промежутка:
    //
    //     [sorted[0].x, sorted[1].x]
    //     [sorted[1].x, sorted[2].x]
    //
    // На каждом из них считаем площадь между двумя граничными функциями.


    for (int i = 0; i < 2; ++i) {
        // ids[0] и ids[1] — номера двух функций,
        // которые ограничивают фигуру на текущем промежутке.
        int ids[2] = {-1, -1};

        // Границы текущего промежутка.
        double a = sorted[i].x;
        double b = sorted[i + 1].x;


        // Определяем, какие две функции являются границами
        // на промежутке [a, b].
        //
        // Если определить не удалось, значит конфигурация корней
        // не соответствует ожидаемой фигуре.
        if (!interval_functions(roots, a, b, ids)) {
            fprintf(stderr, "Cannot determine boundary functions on [%.10f, %.10f]\n", a, b);
            exit(1);
        }

        // Считаем разность интегралов двух граничных функций.
        // Геометрически интеграл разности функций даёт площадь между ними.

        double part = integral(funcs[ids[0]], a, b, EPS_INTEGRAL) - integral(funcs[ids[1]], a, b, EPS_INTEGRAL);
        result += fabs(part);
    }
    return result;
}

