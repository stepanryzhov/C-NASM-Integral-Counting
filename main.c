// Тестовые функции для проверки root() и integral().
// Эти функции не являются функциями из SPEC_FILE.
// Они нужны отдельно, чтобы можно было проверять численные методы
// на простых известных примерах.

static double test_x(double x) { 
    return x; 
}
static double test_dx(double x) { 
    (void)x; return 1.0; 
}
static double test_two(double x) { 
    (void)x; return 2.0; 
}
static double test_dtwo(double x) {
     (void)x; return 0.0; 
}
static double test_x2(double x) { 
    return x * x; 
}
static double test_dx2(double x) { 
    return 2.0 * x; 
}
static double test_line(double x) { 
    return -x + 3.0; 
}
static double test_dline(double x) {
     (void)x; return -1.0; 
}
static double test_sin(double x) { 
    return sin(x); 
}
static double test_dsin(double x) { 
    return cos(x); 
}

static int check_test_id(int id) { 
    return id >= 1 && id <= 5; 
}


static int run_test_root(const char *arg) {
    // Запускает тест функции root().
    //
    // Эта функция вызывается при параметре командной строки:
    //
    //     --test-root F1:F2:A:B:E:R
    //
    // Формат аргумента:
    //     F1:F2:A:B:E:R
    //
    // Где:
    //     F1       — номер первой тестовой функции;
    //     F2       — номер второй тестовой функции;
    //     A        — левая граница отрезка поиска;
    //     B        — правая граница отрезка поиска;
    //     E        — точность eps;
    //     R        — ожидаемый результат.
    //
    // Номера тестовых функций:
    //     1 -> x
    //     2 -> 2
    //     3 -> x*x
    //     4 -> -x+3
    //     5 -> sin(x)
    //
    // Аргументы:
    //     arg — строка с параметрами теста,
    //           например "1:2:0:4:1e-7:2".
    //
    // Возвращает:
    //     0 — тест успешно выполнен;
    //     1 — аргумент имеет неправильный формат.
    //
    // Что выводит:
    //     найденный корень,
    //     абсолютную ошибку,
    //     относительную ошибку.


    // Массив тестовых функций.
    afunc *funcs[5] = {test_x, test_two, test_x2, test_line, test_sin};
    // Массив производных тестовых функций.
    afunc *dfuncs[5] = {test_dx, test_dtwo, test_dx2, test_dline, test_dsin};
    // f_id и g_id — номера двух тестовых функций.
    int f_id, g_id;
    // a, b     — границы отрезка поиска корня;
    

    
    double a, b;
    // eps      — требуемая точность; 
    double eps;
    // expected — ожидаемый ответ;
    double expected;
    // got      — результат, полученный root();
    double got;
    // abs_err  — абсолютная ошибка;
    double abs_err;
    // rel_err  — относительная ошибка.
    // величина относительной ошибки нужна чтобы оценить ошибку в мастштабе ответа
    // например ошибка 0,1 для миллиона маленькая а для 0,2 большая
    double rel_err;
    char tail;


    if (sscanf(arg, "%d:%d:%lf:%lf:%lf:%lf%c", &f_id, &g_id, &a, &b, &eps, &expected, &tail) != 6 ||
        !check_test_id(f_id) || !check_test_id(g_id)) {
        fprintf(stderr, "Неверный --test-root аргумент. Ожидалось F1:F2:A:B:E:R, id функций - 1..5.\n");
        return 1;
    }

    got = root(funcs[f_id - 1], funcs[g_id - 1], dfuncs[f_id - 1], dfuncs[g_id - 1], a, b, eps);
    abs_err = fabs(got - expected);
    rel_err = fabs(expected) > 1e-15 ? abs_err / fabs(expected) : abs_err;
    printf("%.10f %.10f %.10f\n", got, abs_err, rel_err);
    return 0;
}

static int run_test_integral(const char *arg) {
    // Запускает тест функции integral().
    //
    // Эта функция вызывается при параметре командной строки:
    //
    //     --test-integral F:A:B:E:R
    //
    // Формат аргумента:
    //     F:A:B:E:R
    //
    // Где:
    //     F — номер тестовой функции;
    //     A — левая граница интегрирования;
    //     B — правая граница интегрирования;
    //     E — требуемая точность eps;
    //     R — ожидаемый результат.
    //
    // Номера тестовых функций:
    //     1 -> x
    //     2 -> 2
    //     3 -> x*x
    //     4 -> -x+3
    //     5 -> sin(x)
    //
    // Аргументы:
    //     arg — строка с параметрами теста,
    //           например "1:0:2:1e-7:2".
    //
    // Возвращает:
    //     0 — тест успешно выполнен;
    //     1 — аргумент имеет неправильный формат.
    //
    // Что выводит:
    //     найденное значение интеграла,
    //     абсолютную ошибку,
    //     относительную ошибку.
    afunc *funcs[5] = {test_x, test_two, test_x2, test_line, test_sin};
    int f_id;
    double a, b, eps, expected, got, abs_err, rel_err;
    char tail;

    if (sscanf(arg, "%d:%lf:%lf:%lf:%lf%c", &f_id, &a, &b, &eps, &expected, &tail) != 5 ||
        !check_test_id(f_id)) {
        fprintf(stderr, "Неверный --test-integral аргумент. Ожидалось F:A:B:E:R, id функций - 1..5.\n");
        return 1;
    }

    got = integral(funcs[f_id - 1], a, b, eps);
    abs_err = fabs(got - expected);
    rel_err = fabs(expected) > 1e-15 ? abs_err / fabs(expected) : abs_err;
    printf("%.10f %.10f %.10f\n", got, abs_err, rel_err);
    return 0;
}

static void print_roots(void) {
    // Печатает найденные точки пересечения функций.
    // 
    // Аргументы:
    // нет.
    //
    // Возвращает:
    // ничего не возвращает
    //
    // Вызывает find_roots(roots), чтобы найти эти корни.
    // работает уже с ассемблерными функциями, а не тестовыми

    // Печатает каждый корень в формате:
    //        x12 = значение
    //        x13 = значение
    //        x23 = значение
    // Функция нужна для реализации флага -r, -root

    RootInfo roots[3];
    find_roots(roots);
    for (int i = 0; i < 3; ++i) printf("%s = %.10f\n", roots[i].name, roots[i].x);
}

static void print_iterations(void) {
    // Печатает количество итераций, которое потребовалось
    // для нахождения каждой точки пересечения.
    // Эта функция используется для параметра:
    //
    //     -i
    //     --iterations
    //
    // Аргументы:
    // нет.
    //
    // Возвращает:
    // ничего не возвращает.

    RootInfo roots[3];
    find_roots(roots);
    int total = 0;
    for (int i = 0; i < 3; ++i) {
        printf("%s: %d\n", roots[i].name, roots[i].iterations);
        total += roots[i].iterations;
    }
    printf("Всего итераций: %d\n", total);
}

int main(int argc, char **argv) {
    // Главная функция программы integral.
    //
    // Отвечает за обработку параметров командной строки
    // и выбор режима работы программы.

    // Возможные режимы:
    //     без параметров        — посчитать и вывести площадь фигуры;
    //     -h, --help           — вывести справку;
    //     -r, --root           — вывести точки пересечения функций;
    //     -i, --iterations     — вывести количество итераций поиска корней;
    //     -R, --test-root      — запустить тест функции root();
    //     -I, --test-integral  — запустить тест функции integral().
    //
    // --root и --iterations можно совмещать.
    // --help, --test-root, --test-integral завершают программу сразу.
    // Аргументы:
    // argc — количество аргументов командной строки;
    // argv — массив строк с аргументами командной строки.
    //
    // Возвращает:
    // 0 — программа успешно завершилась;
    // 1 — ошибка в параметрах командной строки.


    // Флаг: нужно ли выводить корни x12, x13, x23.
    int need_roots = 0;

    // Флаг: нужно ли выводить количество итераций.
    int need_iterations = 0;

    // Эта таблица нужна для getopt_long().
    //
    // Каждый элемент задаёт один длинный параметр:
    //
    //     "help"          -> --help
    //     "root"          -> --root
    //     "iterations"    -> --iterations
    //     "test-root"     -> --test-root
    //     "test-integral" -> --test-integral
    //
    // no_argument означает, что параметр не требует значения.
    // required_argument означает, что после параметра должен быть аргумент.
    //
    // Последний элемент {NULL, 0, NULL, 0} завершает массив.
    static struct option long_options[] = {
        {"help", no_argument, NULL, 'h'},
        {"root", no_argument, NULL, 'r'},
        {"iterations", no_argument, NULL, 'i'},
        {"test-root", required_argument, NULL, 'R'},
        {"test-integral", required_argument, NULL, 'I'},
        {NULL, 0, NULL, 0}
    };


    // opt — текущий параметр, который вернул getopt_long().
    int opt;
    while ((opt = getopt_long(argc, argv, "hriR:I:", long_options, NULL)) != -1) {
        if (opt == 'h') {
            print_help(argv[0]);
            return 0;
        }

        // раставляем флаги
        else if (opt == 'r') {
            need_roots = 1;
        }

        else if (opt == 'i') {
            need_iterations = 1;
        }

        // Запускаем тест root() и сразу завершаем программу,
        else if (opt == 'R') {
            return run_test_root(optarg);
        }

        // Запускаем тест integral() и сразу завершаем программу.
        else if (opt == 'I') {
            return run_test_integral(optarg);
        }
        
        // Если параметр неизвестный или задан неправильно,
        // getopt_long вернёт специальное значение, обычно '?'.
        //
        // В таком случае печатаем справку и завершаемся с ошибкой.
        else {
            print_help(argv[0]);
            return 1;
        }
    }

    if (need_roots) {
        print_roots();
    }

    if (need_iterations) {
        print_iterations();
    }

    // если не было передно никаких параметров то просто выводим площадь
    if (!need_roots && !need_iterations) {
        printf("%.10f\n", area());
    }

    return 0;
}
